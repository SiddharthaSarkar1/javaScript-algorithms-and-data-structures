// Map()

// 1. Transforma each element of original array into a new value.
// 2. Returns a new array with the same length as the original array.
// 3. Does not modify the original array
// 4. Used for data transformation, conversion, or computation

const numbers = [1, 2, 3, 4, 5, 6, 7];
const doubledNumbers = numbers.map((elements) => elements * 2);
console.log(doubledNumbers);
